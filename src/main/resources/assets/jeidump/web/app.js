    function pageButtons(active, total) {
        const out = new Set([0, total - 1, active - 1, active, active + 1]);
        const sorted = Array.from(out).filter(value => value >= 0 && value < total).sort((left, right) => left - right);
        const result = [];
        for (let i = 0; i < sorted.length; i++) {
            if (i > 0 && sorted[i] !== sorted[i - 1] + 1) result.push('...');
            result.push(sorted[i]);
        }
        return result;
    }

    function wireSearch() {
        const input = byId('search');
        const results = byId('search-results');
        let activeRow = -1;

        input.addEventListener('input', () => {
            const query = input.value.trim().toLowerCase();
            if (!query) {
                clearSearchResults();
                activeRow = -1;
                return;
            }

            const hits = scoreSearch(query).slice(0, 50);
            results.replaceChildren();
            if (hits.length === 0) {
                results.appendChild(node('div', 'row muted', t('jeidump.web.search.no_matches')));
                results.hidden = false;
                activeRow = -1;
                return;
            }

            for (const hit of hits) {
                const row = buildSearchRow(hit);
                row.addEventListener('mousedown', event => {
                    event.preventDefault();
                    activate(row.dataset.action, row.dataset.target);
                    clearSearchResults();
                    input.value = '';
                });
                results.appendChild(row);
            }

            results.hidden = false;
            activeRow = -1;
        });

        input.addEventListener('keydown', event => {
            const rows = results.querySelectorAll('.row[data-action]');
            if (event.key === 'ArrowDown') {
                event.preventDefault();
                activeRow = Math.min(rows.length - 1, activeRow + 1);
                highlightRow(rows, activeRow);
            } else if (event.key === 'ArrowUp') {
                event.preventDefault();
                activeRow = Math.max(0, activeRow - 1);
                highlightRow(rows, activeRow);
            } else if (event.key === 'Enter') {
                if (activeRow >= 0 && rows[activeRow]) {
                    activate(rows[activeRow].dataset.action, rows[activeRow].dataset.target);
                    clearSearchResults();
                    input.value = '';
                }
            } else if (event.key === 'Escape') {
                clearSearchResults();
            }
        });

        document.addEventListener('click', event => {
            if (!results.contains(event.target) && event.target !== input) {
                clearSearchResults();
            }
        });
    }

    function buildSearchRow(hit) {
        const row = node('div', 'row');
        row.dataset.action = hit.entry.type === 'category' ? 'cat' : 'ingredient';
        row.dataset.target = hit.entry.type === 'category' ? hit.entry.target.cat : hit.entry.target.ingredient;

        if (hit.entry.img) {
            const image = node('img');
            image.src = hit.entry.img;
            image.alt = '';
            row.appendChild(image);
        } else {
            row.appendChild(node('span', 'search-placeholder'));
        }

        row.appendChild(node('span', null, hit.entry.label));
        row.appendChild(node('span', 'meta', searchTypeLabel(hit.entry.type) + ' | ' + (hit.entry.mod || t('jeidump.web.unknown_mod'))));
        return row;
    }

