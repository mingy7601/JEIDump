    function scoreSearch(query) {
        const out = [];
        for (const entry of SEARCH) {
            let score = 0;
            const index = entry.key.indexOf(query);
            if (index === 0) score = 100;
            else if (index > 0) score = 50 - Math.min(index, 30);
            else if (subseq(query, entry.key)) score = 20;
            else continue;

            if (entry.type === 'category') score += 5;
            out.push({ entry, score });
        }

        out.sort((left, right) => right.score - left.score || left.entry.label.length - right.entry.label.length);
        return out;
    }

    function subseq(needle, haystack) {
        let j = 0;
        for (let i = 0; i < haystack.length && j < needle.length; i++) {
            if (haystack[i] === needle[j]) j++;
        }
        return j === needle.length;
    }

    function activate(kind, target) {
        if (kind === 'cat') {
            location.hash = 'cat=' + encodeURIComponent(target);
            return;
        }

        // From the search box, default to "recipes that produce" the ingredient (matches
        // JEI's left-click on an item in the ingredient list).
        if (kind === 'ingredient') jumpTo(target, 'for');
    }

    /**
     * Navigate to the ingredient-focus view. mode is 'for' (recipes that produce it; matches
     * JEI's left-click) or 'use' (recipes that consume it; matches JEI's right-click).
     */
    function jumpTo(id, mode) {
        location.hash = 'ing=' + encodeURIComponent(id) + '&mode=' + (mode === 'use' ? 'use' : 'for');
    }

    /** Wire hotspot hover (tooltip) + left-click (recipes-for) + right-click (uses-of). */
    function wireHotspots(root) {
        const tip = ensureTooltipEl();
        root.querySelectorAll('.hotspot').forEach(spot => {
            spot.addEventListener('mouseenter', () => showTooltip(tip, spot));
            spot.addEventListener('mousemove', event => positionTooltip(tip, event));
            spot.addEventListener('mouseleave', () => {
                tip.hidden = true;
            });
            spot.addEventListener('click', event => {
                event.preventDefault();
                jumpTo(spot.dataset.id, 'for');
            });
            spot.addEventListener('contextmenu', event => {
                event.preventDefault();
                jumpTo(spot.dataset.id, 'use');
            });
            spot.addEventListener('keydown', event => {
                if (event.key === 'Enter') {
                    event.preventDefault();
                    jumpTo(spot.dataset.id, event.shiftKey ? 'use' : 'for');
                }
            });
        });
    }

    /**
     * R / U keybinds while a card is focused (Tab navigates between cards).
     * R = recipes for the first OUTPUT of the focused card (matches JEI's R-on-hover).
     * U = uses of the first INPUT.
     */
    function wireCardKeybinds(root) {
        root.querySelectorAll('.recipe-card').forEach(card => {
            card.addEventListener('keydown', event => {
                if (event.target.classList.contains('hotspot')) return;

                if (event.key === 'r' || event.key === 'R') {
                    const id = card.dataset.firstOut;
                    if (id) {
                        event.preventDefault();
                        jumpTo(id, 'for');
                    }
                } else if (event.key === 'u' || event.key === 'U') {
                    const id = card.dataset.firstIn;
                    if (id) {
                        event.preventDefault();
                        jumpTo(id, 'use');
                    }
                }
            });
        });
    }

    function ensureTooltipEl() {
        let tip = document.getElementById('jei-tooltip');
        if (tip) return tip;

        tip = node('div', 'jei-tooltip');
        tip.id = 'jei-tooltip';
        tip.hidden = true;
        document.body.appendChild(tip);
        return tip;
    }

    function showTooltip(tip, spot) {
        const id = spot.dataset.id;
        const meta = DATA.items[id] || DATA.fluids[id];
        if (!meta) {
            tip.hidden = true;
            return;
        }

        // First line = display name (white), rest dimmed (matches vanilla aesthetic).
        const lines = meta.tooltip && meta.tooltip.length ? meta.tooltip : [meta.name];
        tip.replaceChildren();
        tip.appendChild(node('div', 'tt-name', lines[0]));
        for (let i = 1; i < lines.length; i++) {
            tip.appendChild(node('div', 'tt-line', lines[i]));
        }
        if (meta.mod) tip.appendChild(node('div', 'tt-mod', meta.mod));
        tip.hidden = false;
    }

    function positionTooltip(tip, event) {
        const pad = 14;
        let x = event.clientX + pad;
        let y = event.clientY + pad;
        const rect = tip.getBoundingClientRect();
        if (x + rect.width > window.innerWidth - 4) x = event.clientX - rect.width - pad;
        if (y + rect.height > window.innerHeight - 4) y = event.clientY - rect.height - pad;
        tip.style.left = Math.max(4, x) + 'px';
        tip.style.top = Math.max(4, y) + 'px';
    }

    function applyHash() {
        if (!DATA) return;

        const params = parseHash();
        if (params.ing) {
            renderIngredient(params.ing, params.mode === 'use' ? 'use' : 'for');
            return;
        }

        if (!params.cat) {
            renderHome();
            return;
        }

        renderCategory(params.cat, params.p ? parseInt(params.p, 10) : 0);
    }

    function parseHash() {
        const hash = location.hash.replace(/^#/, '');
        const out = {};
        for (const part of hash.split('&')) {
            const eq = part.indexOf('=');
            if (eq < 0) continue;
            out[part.substring(0, eq)] = decodeURIComponent(part.substring(eq + 1));
        }
        return out;
    }

    function chooseUiLocale() {
        const available = getAvailableUiLocales();
        return matchAvailableLocale(requestedLocaleFromUrl(), available)
            || matchAvailableLocale(requestedLocaleFromHost(), available)
            || matchAvailableLocale(requestedLocaleFromBrowser(), available)
            || matchAvailableLocale(DEFAULT_UI_LOCALE, available)
            || available[0]
            || DEFAULT_UI_LOCALE;
    }

    function requestedLocaleFromUrl() {
        const params = new URLSearchParams(location.search);
        return params.get('lang') || params.get('locale');
    }

    function requestedLocaleFromHost() {
        const host = (location.hostname || '').toLowerCase();
        if (!host) return null;
        return host.split('.')[0];
    }

    function requestedLocaleFromBrowser() {
        return navigator.language || navigator.userLanguage || null;
    }

    function getAvailableUiLocales() {
        const locales = [];
        const advertised = Array.isArray(I18N.availableLocales) ? I18N.availableLocales : [];
        for (const locale of advertised) pushLocale(locales, locale);

        const tables = I18N.translations || {};
        for (const locale in tables) pushLocale(locales, locale);

        pushLocale(locales, DEFAULT_UI_LOCALE);
        locales.sort(compareLocales);
        return locales;
    }

    function buildDataLocaleFallbacks(locale) {
        const fallbacks = [];
        pushLocale(fallbacks, locale);
        pushLocale(fallbacks, DEFAULT_DATA_LOCALE);
        pushLocale(fallbacks, MANIFEST.latestDumpLocale);
        if (Array.isArray(MANIFEST.availableDataLocales)) {
            for (const candidate of MANIFEST.availableDataLocales) {
                pushLocale(fallbacks, candidate);
            }
        }
        return fallbacks;
    }

    function manifestHasDataLocales() {
        return Array.isArray(MANIFEST.availableDataLocales) && MANIFEST.availableDataLocales.length > 0;
    }

    function manifestDeclaresLocale(locale) {
        return Array.isArray(MANIFEST.availableDataLocales)
            && MANIFEST.availableDataLocales.map(normalizeLocaleCode).indexOf(normalizeLocaleCode(locale)) >= 0;
    }

    function loadScript(src) {
        if (SCRIPT_CACHE.has(src)) return SCRIPT_CACHE.get(src);

        const promise = new Promise((resolve, reject) => {
            const script = document.createElement('script');
            script.src = src;
            script.async = true;
            script.onload = () => resolve();
            script.onerror = () => {
                SCRIPT_CACHE.delete(src);
                reject(new Error('could not load script'));
            };
            document.body.appendChild(script);
        });

        SCRIPT_CACHE.set(src, promise);
        return promise;
    }

    function t(key) {
        const args = Array.prototype.slice.call(arguments, 1);
        const localeTable = (I18N.translations && I18N.translations[uiLocale]) || {};
        const fallbackTable = (I18N.translations && I18N.translations[DEFAULT_UI_LOCALE]) || FALLBACK_TRANSLATIONS;
        const template = localeTable[key] || fallbackTable[key] || key;
        return format(template, args);
    }

    function format(template, args) {
        const placeholder = '__PERCENT__';
        let autoIndex = 0;
        return String(template)
            .replace(/%%/g, placeholder)
            .replace(/%((\d+)\$)?[sd]/g, function (match, _full, explicitIndex) {
                const index = explicitIndex ? parseInt(explicitIndex, 10) - 1 : autoIndex++;
                return index >= 0 && index < args.length ? String(args[index]) : match;
            })
            .replace(new RegExp(placeholder, 'g'), '%');
    }

