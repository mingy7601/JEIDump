        mainLine.appendChild(link);
        mainLine.appendChild(document.createTextNode('. ' + t('jeidump.web.footer.open_local')));
        footer.appendChild(mainLine);

        const updatedAt = formatDumpTimestamp();
        if (!updatedAt) return;

        footer.appendChild(node('div', 'footer-line footer-updated', t('jeidump.web.footer.updated_at', updatedAt)));
