    function matchAvailableLocale(requested, available) {
        const normalized = normalizeLocaleCode(requested);
        if (!normalized) return null;
        if (available.indexOf(normalized) >= 0) return normalized;

        const base = normalized.split('_')[0];
        for (const locale of available) {
            if (locale.split('_')[0] === base) return locale;
        }

        return null;
    }

    function pushLocale(locales, locale) {
        const normalized = normalizeLocaleCode(locale);
        if (!normalized || locales.indexOf(normalized) >= 0) return;
        locales.push(normalized);
    }

    function normalizeLocaleCode(locale) {
        if (!locale) return null;
        const normalized = String(locale).trim().toLowerCase().replace(/-/g, '_');
        return normalized || null;
    }

    function compareLocales(left, right) {
        if (left === DEFAULT_UI_LOCALE) return -1;
        if (right === DEFAULT_UI_LOCALE) return 1;
        return left.localeCompare(right);
    }

    function byId(id) {
        return document.getElementById(id);
    }

    function node(tagName, className, text) {
        const element = document.createElement(tagName);
        if (className) element.className = className;
        if (typeof text === 'string') element.textContent = text;
        return element;
    }

    function cssEscape(value) {
        return String(value).replace(/(["\\])/g, '\\$1');
    }
