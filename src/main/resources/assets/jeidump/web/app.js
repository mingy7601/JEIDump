    function formatDumpTimestamp() {
        if (!DATA || !DATA.generatedAt) return '';

        const date = new Date(DATA.generatedAt);
        if (Number.isNaN(date.getTime())) return String(DATA.generatedAt);

        const locale = (uiLocale || DEFAULT_UI_LOCALE).replace(/_/g, '-');
        const options = { dateStyle: 'medium', timeStyle: 'short' };

        if (typeof Intl !== 'undefined' && typeof Intl.DateTimeFormat === 'function') {
            try {
                return new Intl.DateTimeFormat(locale, options).format(date);
            } catch (error) {
                return date.toLocaleString();
            }
        }

        return date.toLocaleString();
    }

    function populateLocaleSelect() {
        const select = byId('locale-select');
        const locales = getAvailableUiLocales();
        select.replaceChildren();

        for (const locale of locales) {
            const option = node('option', null, locale);
            option.value = locale;
            select.appendChild(option);
        }

        select.value = uiLocale;
        select.disabled = locales.length <= 1;
    }

    function wireLocaleSelect() {
        byId('locale-select').addEventListener('change', function () {
            const locale = normalizeLocaleCode(this.value);
            if (!locale || locale === uiLocale) {
                updateLocaleUrl(uiLocale);
                return;
            }

            uiLocale = locale;
            updateLocaleUrl(locale);
            applyChromeTranslations();
            populateLocaleSelect();
            loadDataForUiLocale(locale);
        });
    }

    function loadDataForUiLocale(locale) {
        const generation = ++loadGeneration;
        DATA = null;
        dataLocale = null;
        SEARCH = [];
        clearSidebarSelection();
        clearSearchResults();
        renderFooter();
        renderLoadingState();

        loadFirstDataLocale(buildDataLocaleFallbacks(locale), 0, null)
            .then(result => {
                if (generation !== loadGeneration) return;

                DATA = result.data;
                dataLocale = result.locale;
                buildSearchIndex();
                renderSidebar();
                renderFooter();
                applyHash();
            })
            .catch(error => {
                if (generation !== loadGeneration) return;

                renderFooter();
                renderLoadError(locale, error);
            });
    }

    function loadFirstDataLocale(locales, index, lastError) {
        if (index >= locales.length) {
            return Promise.reject(lastError || new Error('no dump data available'));
        }

        const locale = locales[index];
        return loadDataLocale(locale)
            .then(data => ({ locale, data }))
            .catch(error => loadFirstDataLocale(locales, index + 1, error));
    }

    function loadDataLocale(locale) {
        const normalized = normalizeLocaleCode(locale);
        if (DATA_CACHE.has(normalized)) {
            return Promise.resolve(DATA_CACHE.get(normalized));
        }

        if (manifestHasDataLocales() && !manifestDeclaresLocale(normalized)) {
            return Promise.reject(new Error('no dump bundle declared for ' + normalized));
        }

        const src = 'data/locales/' + encodeURIComponent(normalized) + '/index.js';
        return loadScript(src).then(() => {
            const datasets = window.__JEI_DUMP_DATASETS || {};
            const data = datasets[normalized];
            if (!data) throw new Error('script loaded but did not register ' + normalized);

            DATA_CACHE.set(normalized, data);
            return data;
        }).catch(error => {
            throw new Error(src + ' - ' + error.message);
        });
    }

    function buildSearchIndex() {
        SEARCH = [];
        for (const cat of DATA.categories) {
            SEARCH.push({
                key: (cat.title + ' ' + cat.modName + ' ' + cat.uid).toLowerCase(),
                label: cat.title,
                mod: cat.modName,
                type: 'category',
                target: { cat: cat.id }
            });
        }

        for (const id in DATA.items) {
            const item = DATA.items[id];
            SEARCH.push({
                key: (item.name + ' ' + item.mod + ' ' + id).toLowerCase(),
                label: item.name,
                mod: item.mod,
                img: item.img,
                type: 'item',
                target: { ingredient: id }
            });
        }

        for (const id in DATA.fluids) {
            const fluid = DATA.fluids[id];
            SEARCH.push({
                key: (fluid.name + ' ' + fluid.mod + ' ' + id).toLowerCase(),
                label: fluid.name,
                mod: fluid.mod,
                img: fluid.img,
                type: 'fluid',
                target: { ingredient: id }
            });
        }
    }

    function renderSidebar() {
        const list = byId('category-list');
        list.replaceChildren();

        const byMod = new Map();
        for (const cat of DATA.categories) {
            const key = cat.modName || t('jeidump.web.unknown_mod');
            if (!byMod.has(key)) byMod.set(key, []);
            byMod.get(key).push(cat);
        }
        // Sort mods alphabetically; Minecraft first if present.
        const mods = Array.from(byMod.keys()).sort((left, right) => {
            if (left === 'Minecraft') return -1;
            if (right === 'Minecraft') return 1;
            return left.localeCompare(right);
        });

        for (const modName of mods) {
            const header = node('li');
            header.appendChild(node('div', 'group-title', modName));
            list.appendChild(header);

            const cats = byMod.get(modName).slice().sort((left, right) => left.title.localeCompare(right.title));
            for (const cat of cats) {
                const li = node('li');
                li.dataset.cat = cat.id;

                const link = node('a');
                link.href = '#cat=' + encodeURIComponent(cat.id);
                link.appendChild(document.createTextNode(cat.title));
                link.appendChild(node('span', 'count', String(cat.recipeCount)));

                li.appendChild(link);
                list.appendChild(li);
            }
        }
    }

